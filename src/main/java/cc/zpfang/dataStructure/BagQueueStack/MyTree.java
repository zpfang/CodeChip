    public static String trimTwoZero(String str) {
        if (str == null) {
            return "";
        }
        StringBuilder stringBuilder = new StringBuilder(str);
        if (str.length() < 6) {
            stringBuilder.append("000000");
        }
        int offset = 6;
        while (stringBuilder.length() > 1) {
            if ("00".equals(stringBuilder.substring(offset - 2, offset))) {
                stringBuilder.delete(offset - 2, stringBuilder.length());
            }else{
                break;
            }
            offset -= 2;
        }
        return stringBuilder.toString();
    }

